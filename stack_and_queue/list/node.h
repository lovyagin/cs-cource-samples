/**
 * @file node.h
 * @brief Узел односвязного списка для хранения целых чисел.
 *
 */

#ifndef NODE_H
#define NODE_H

/**
 * @brief Узел односвязного списка.
 *
 * Структура представляет один элемент списка и содержит:
 * - значение типа int
 * - указатель на следующий узел списка
 */
typedef struct node node;

/**
 * @struct node
 * @brief Определение структуры узла.
 */
struct node
{
    int data; /**< Значение, хранящееся в узле */
    node *
        next; /**< Указатель на следующий узел списка (NULL если последний) */
};

#endif /* NODE_H */
