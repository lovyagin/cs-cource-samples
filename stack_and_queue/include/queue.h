/**
 * @file queue.h
 * @brief Очередь целых чисел.
 *
 * Модуль реализует динамическую очередь целых чисел.
 * Поддерживаются стандартные операции: push, pop, проверка на пустоту/полноту и копирование.
 *
 * Особенности:
 * - Динамическое выделение памяти по мере необходимости
 * - Полная инкапсуляция структуры очереди
 *
 * Потокобезопасность:
 * - Очередь не является потокобезопасной.
 *
 * Обработка ошибок:
 * - Функции возвращают 0 при успешном выполнении или отрицательное значение при ошибке.
 * - Для push/pop ошибки могут возникать при нехватке памяти или пустой очереди.
 */

#ifndef QUEUE_H
#define QUEUE_H

#include <stddef.h>

/**
 * @brief Неполный тип очереди.
 */
typedef struct queue queue;

/* =========================================================
   Создание и уничтожение
   ========================================================= */

/**
 * @brief Создает новую пустую очередь.
 *
 * @return Указатель на очередь или NULL при ошибке выделения памяти.
 *
 * @warning Созданная очередь должна быть освобождена с помощью queue_free().
 */
queue *queue_create(void);

/**
 * @brief Создает полную копию очереди.
 *
 * @param[in] q Исходная очередь.
 * @return Новый экземпляр очереди или NULL при ошибке.
 *
 * @warning Созданная очередь должна быть освобождена с помощью queue_free().
 */
queue *queue_copy(const queue *q);

/**
 * @brief Освобождает очередь и все ее ресурсы.
 *
 * @param[in,out] q Очередь.
 */
void queue_free(queue *q);

/* =========================================================
   Проверка состояния
   ========================================================= */

/**
 * @brief Проверяет, пуста ли очередь.
 *
 * @param[in] q Очередь.
 * @return 1 если очередь пуста, 0 иначе.
 */
int queue_is_empty(const queue *q);

/**
 * @brief Проверяет, полна ли очередь.
 *
 * Для динамической очереди обычно всегда возвращает 0,
 * но может использоваться для проверки ограничений по максимальной вместимости.
 *
 * @param[in] q Очередь.
 * @return 1 если очередь полна, 0 иначе.
 */
int queue_is_full(const queue *q);

/* =========================================================
   Работа с элементами
   ========================================================= */

/**
 * @brief Добавляет элемент в конец очереди (push).
 *
 * @param[in,out] q Очередь.
 * @param[in] val Значение для добавления.
 *
 * @return 0 при успехе, отрицательное значение при ошибке (например, нехватка памяти).
 */
int queue_push(queue *q, int val);

/**
 * @brief Извлекает элемент из начала очереди (pop).
 *
 * @param[in,out] q Очередь.
 * @param[out] val Указатель для сохранения извлеченного значения (может быть NULL).
 *
 * @return 0 при успехе, отрицательное значение при ошибке (например, очередь пуста).
 */
int queue_pop(queue *q, int *val);

/**
 * @brief Получает элемент в конце очереди без удаления.
 *
 * @param[in] q Очередь.
 * @return Указатель на элемент при успехе, либо NULL, если очередь пуста.
 */
int *queue_tail(queue *q);

/**
 * @brief Получает элемент в начале очереди без удаления.
 *
 * @param[in] q Очередь.
 * @return Указатель на элемент при успехе, либо NULL, если очередь пуста.
 */
int *queue_head(queue *q);

/**
 * @brief Константная версия queue_tail.
 *
 * @param[in] q Очередь.
 * @return Указатель на элемент при успехе, либо NULL, если очередь пуста.
 */
const int *queue_tail_const(const queue *q);

/**
 * @brief Констатная версия queue_head.
 *
 * @param[in] q Очередь.
 * @return Указатель на элемент при успехе, либо NULL, если очередь пуста.
 */
const int *queue_head_const(const queue *q);

#endif // QUEUE_H
