/**
 * @file stack.h
 * @brief Стек целых чисел.
 *
 * Модуль реализует динамический стек целых чисел.
 * Стек поддерживает стандартные операции: push, pop, проверку на пустоту/полноту и копирование.
 *
 * Особенности:
 * - Динамическое выделение памяти по мере необходимости
 * - Полная инкапсуляция структуры стека
 *
 * Потокобезопасность:
 * - Стек не является потокобезопасным.
 *
 * Обработка ошибок:
 * - Функции возвращают 0 при успешном выполнении или отрицательное значение при ошибке.
 * - Для push/pop ошибки могут возникать при недостатке памяти или пустом стеке.
 */

#ifndef STACK_H
#define STACK_H

#include <stddef.h>

/**
 * @brief Неполный тип стека.
 */
typedef struct stack stack;

/* =========================================================
   Создание и уничтожение
   ========================================================= */

/**
 * @brief Создает новый пустой стек.
 *
 * @return Указатель на стек или NULL при ошибке выделения памяти.
 *
 * @warning Созданный стек должен быть освобожден с помощью stack_free().
 */
stack *stack_create(void);

/**
 * @brief Создает полную копию стека.
 *
 * @param[in] s Исходный стек.
 * @return Новый стек или NULL при ошибке.
 *
 * @warning Созданный стек должен быть освобожден с помощью stack_free().
 */
stack *stack_copy(const stack *s);

/**
 * @brief Освобождает стек и все его ресурсы.
 *
 * @param[in,out] s Указатель на стек.
 */
void stack_free(stack *s);

/* =========================================================
   Проверка состояния
   ========================================================= */

/**
 * @brief Проверяет, пуст ли стек.
 *
 * @param[in] s Стек.
 * @return 1 если стек пуст, 0 иначе.
 */
int stack_is_empty(const stack *s);

/**
 * @brief Проверяет, полон ли стек.
 *
 * @param[in] s Стек.
 * @return 1 если стек полон, 0 иначе.
 */
int stack_is_full(const stack *s);

/* =========================================================
   Работа с элементами
   ========================================================= */

/**
 * @brief Добавляет элемент в стек (push).
 *
 * @param[in,out] s Стек.
 * @param[in] val Значение для добавления.
 *
 * @return 0 при успехе, отрицательное значение при ошибке (например, нехватка памяти).
 */
int stack_push(stack *s, int val);

/**
 * @brief Извлекает элемент из стека (pop).
 *
 * @param[in,out] s Стек.
 * @param[out] val Указатель для сохранения извлеченного значения (может быть NULL).
 *
 * @return 0 при успехе, отрицательное значение при ошибке (например, стек пуст).
 */
int stack_pop(stack *s, int *val);

/**
 * @brief Получает элемент на вершине стека без извлечения.
 *
 * @param[in] s Стек.
 * @param[out] val Указатель для сохранения значения.
 * @return Указатель на элемент при успехе, NULL если стек пуст.
 */
int *stack_top(stack *s);

/**
 * @brief Константная версия stack_top.
 *
 * @param[in] s Стек.
 * @param[out] val Указатель для сохранения значения.
 * @return Указатель на элемент при успехе, NULL если стек пуст.
 */
const int *stack_top_const(const stack *s);

#endif // STACK_H
