/**
 * @file clist.h
 * @brief Односвязный циклический список с указателем на текущий элемент.
 *
 * Модуль реализует односвязный циклический список целых чисел.
 * В списке хранится только один указатель (текущий элемент).
 *
 * Особенности:
 * - Кольцевая структура: последний элемент указывает на первый.
 * - Все вставки/удаления выполняются после текущего элемента за O(1).
 * - Получение следующего элемента перемещает текущий указатель.
 *
 * Потокобезопасность:
 * - Список не является потокобезопасным.
 *
 * Обработка ошибок:
 * - Функции возвращают NULL или отрицательные значения при ошибках.
 */

#ifndef CLIST_H
#define CLIST_H

#include <stddef.h>

/**
 * @brief Неполный тип узла списка.
 */
typedef struct clist_node clist_node;

/**
 * @brief Неполный тип списка.
 */
typedef struct clist clist;

/* =========================================================
   Создание и уничтожение
   ========================================================= */

/**
 * @brief Создает пустой циклический список.
 *
 * @return Указатель на список или NULL при ошибке выделения памяти.
 */
clist *clist_create(void);

/**
 * @brief Освобождает список и все его узлы.
 *
 * @param[in,out] lst Список.
 */
void clist_free(clist *lst);

/**
 * @brief Создает полную копию списка.
 *
 * @param[in] src Исходный список.
 * @return Новый список или NULL при ошибке выделения памяти.
 */
clist *clist_copy(const clist *src);

/* =========================================================
   Проверка состояния
   ========================================================= */

/**
 * @brief Проверяет, пуст ли список.
 *
 * @param[in] lst Список.
 * @return 1 если список пуст, 0 иначе.
 */
int clist_is_empty(const clist *lst);

/* =========================================================
   Работа с текущим элементом
   ========================================================= */

/**
 * @brief Возвращает указатель на значение текущего элемента.
 *
 * @param[in] lst Список.
 * @return Указатель на значение или NULL, если список пуст.
 */
int *clist_current(clist *lst);

/**
 * @brief Константная версия clist_current.
 */
const int *clist_current_const(const clist *lst);

/**
 * @brief Переходит к следующему элементу и возвращает его.
 *
 * @param[in,out] lst Список.
 * @return Указатель на значение следующего элемента или NULL, если список пуст.
 */
int *clist_next(clist *lst);

/**
 * @brief Вставляет новый элемент после текущего.
 *
 * @param[in,out] lst Список.
 * @param[in] val Значение для вставки.
 * @return 0 при успехе, отрицательное значение при ошибке (например, нехватка памяти).
 */
int clist_insert_after(clist *lst, int val);

/**
 * @brief Удаляет элемент, следующий за текущим.
 *
 * @param[in,out] lst Список.
 * @param[out] val Указатель для сохранения удаленного значения (может быть NULL).
 * @return 0 при успехе, отрицательное значение при ошибке (например, список пуст или только один элемент).
 */
int clist_remove_after(clist *lst, int *val);

#endif // CLIST_H
