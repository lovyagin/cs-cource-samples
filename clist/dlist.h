/**
 * @file dlist.h
 * @brief Двусвязный список целых чисел с текущим элементом.
 *
 * Модуль реализует двусвязный список с полной инкапсуляцией структуры.
 * Все операции выполняются относительно текущего элемента:
 * - переход к следующему и предыдущему элементу,
 * - вставка перед или после текущего элемента,
 * - удаление текущего элемента,
 * - получение текущего элемента.
 *
 * Потокобезопасность:
 * - Список не является потокобезопасным.
 *
 * Обработка ошибок:
 * - Функции возвращают NULL или отрицательное значение при ошибке (например, память не выделена или список пуст).
 */

#ifndef DLIST_H
#define DLIST_H

#include <stddef.h>

/**
 * @brief Неполный тип двусвязного списка.
 */
typedef struct dlist dlist;

/* =========================================================
   Создание и уничтожение
   ========================================================= */

/**
 * @brief Создает пустой двусвязный список.
 *
 * @return Указатель на список или NULL при ошибке выделения памяти.
 */
dlist *dlist_create(void);

/**
 * @brief Освобождает список и все его узлы.
 *
 * @param[in,out] lst Список.
 */
void dlist_free(dlist *lst);

/**
 * @brief Создает полную копию списка.
 *
 * @param[in] src Исходный список.
 * @return Новый список или NULL при ошибке выделения памяти.
 */
dlist *dlist_copy(const dlist *src);

/* =========================================================
   Проверка состояния
   ========================================================= */

/**
 * @brief Проверяет, пуст ли список.
 *
 * @param[in] lst Список.
 * @return 1 если список пуст, 0 иначе.
 */
int dlist_is_empty(const dlist *lst);

/* =========================================================
   Работа с текущим элементом
   ========================================================= */

/**
 * @brief Возвращает указатель на значение текущего элемента.
 *
 * @param[in] lst Список.
 * @return Указатель на значение или NULL, если список пуст.
 */
int *dlist_current(dlist *lst);

/**
 * @brief Константная версия dlist_current.
 *
 * @param[in] lst Список.
 * @return Указатель на значение или NULL, если список пуст.
 */
const int *dlist_current_const(const dlist *lst);

/**
 * @brief Переходит к следующему элементу и возвращает его.
 *
 * @param[in,out] lst Список.
 * @return Указатель на значение следующего элемента или NULL, если список пуст.
 */
int *dlist_next(dlist *lst);

/**
 * @brief Переходит к предыдущему элементу и возвращает его.
 *
 * @param[in,out] lst Список.
 * @return Указатель на значение предыдущего элемента или NULL, если список пуст.
 */
int *dlist_prev(dlist *lst);

/**
 * @brief Вставляет новый элемент после текущего.
 *
 * @param[in,out] lst Список.
 * @param[in] val Значение для вставки.
 * @return 0 при успехе, отрицательное значение при ошибке.
 */
int dlist_insert_after(dlist *lst, int val);

/**
 * @brief Вставляет новый элемент перед текущим.
 *
 * @param[in,out] lst Список.
 * @param[in] val Значение для вставки.
 * @return 0 при успехе, отрицательное значение при ошибке.
 */
int dlist_insert_before(dlist *lst, int val);

/**
 * @brief Удаляет текущий элемент и делает текущим следующий элемент.
 *
 * @param[in,out] lst Список.
 * @param[out] val Указатель для сохранения удаленного значения (может быть NULL).
 * @return 0 при успехе, отрицательное значение при ошибке (например, список пуст).
 */
int dlist_remove_current(dlist *lst, int *val);

#endif // DLIST_H
